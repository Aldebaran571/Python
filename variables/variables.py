#definiendo una variable
nombre = "john"
#concatenar con f-strings
bienvenido = f"hola  {nombre}  como va la causa?" 
#concatenar con +
bienvenido = "hola" +nombre+ "como va todo?" 
print ("hola" in bienvenido)#true
print ("hola" not in bienvenido)#false
#operadores de pertenencia el (in - not in) se usan como si fueran valores booleanos en las variables