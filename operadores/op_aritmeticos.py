#suma y resta (+ y -)
suma = 12 + 5
resta = 12 - 5
#multiplicacion y division (* y /)
multi = 12 * 5
division  = 12 / 5 #la divisdion devuelve un dato tipo float

#potenciacion exponente (**)
exponente = 12 ** 5

#division baja? (//)
division_baja = 12 // 5 #devuelve entero redondea hacia abajo   

#porcentaje o modulo (%)
modulo = 12 % 5
print(suma, resta, multi, division, exponente, division_baja, modulo)

